export const i18nState = reactive({
  language: (localStorage.getItem('language') as Language) || 'zh',
  translations: {} as Record<string, { zh: string; en: string }>,
});

export async function loadTranslations() {
  try {
    const { data } = await http.get<TranslationRow[]>('/translations');
    i18nState.translations = Object.fromEntries(
      data
        .filter((row) => !fallback[row.key] || !/[�鐢浠诲椤圭洰鎶ヨ〃鍚庡彴]/.test(row.zh))
        .map((row) => [row.key, { zh: row.zh, en: row.en }]),
    );
  } catch {
    i18nState.translations = {};
  }
}

export function t(key: string) {
  const row = i18nState.translations[key] || fallback[key];
  return row?.[i18nState.language] || key;
}

export function setLocalLanguage(language: Language) {
  i18nState.language = language;
  localStorage.setItem('language', language);
  const currentUser = JSON.parse(localStorage.getItem('currentUser') || '{}');
  localStorage.setItem('currentUser', JSON.stringify({ ...currentUser, language }));
}
